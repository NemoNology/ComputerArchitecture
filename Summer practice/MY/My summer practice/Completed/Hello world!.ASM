; FORMAT - назначение типа выходного файла
; MZ - исполняемые файлы DOS
; ORG - устранавливает адрес в памяти,
; по которому загружается следующий за ней код
FORMAT	MZ
ORG 100H

; cout str1 (In C++)
; AH - в этом регистре устранавливается код функции
; DS:DX - обычно, через эти регистры передаются параметры функции
; 09H - Вывод строки символов на экран
; INT - комманда программного прерывания
; INT 21H - Запрос функций DOS
; INT 20H - Завершение программы
;MOV AH, 09H
;MOV DX, str1
;INT 21H


MOV SI, str1


M1:
	MOV AH, 02H
	MOV DX, [SI]
	INT 21H

	INC SI

	CMP SI, '$'
	JNE M1



; system("pause") (In C++)
; 01H - Ввод строки с клавиатуры с эхом на экран
MOV AH, 01H
INT 21H

; Закрытие консоли
; (Завершение процесса программы с передачей управления)
MOV AH, 4CH
INT 21H

; Инициализация переменной str1 через DB - Define Byte -
; Пресвдокомманда инициализации однобайтовой переменной
str1 DB 'Hello world!', '$'